# 正则表达式
# **正则表达式常用字符速查**

# **1. 单个字符匹配**
# `.`  匹配除换行符外的任意单个字符
# `\d` 匹配数字，等价于 `[0-9]`
# `\D` 匹配非数字，等价于 `[^0-9]`
# `\w` 匹配字母、数字、下划线，等价于 `[A-Za-z0-9_]`
# `\W` 匹配非字母、数字、下划线
# `\s` 匹配空白字符（空格、制表符、换行符等）
# `\S` 匹配非空白字符

# **2. 字符集合**  [ ] 内的字符表示一个字符集合
# `[abc]`  匹配 a、b 或 c 中的任意一个字符
# `[^abc]` 匹配除 a、b、c 外的任意字符
# `[a-z]`  匹配 a 到 z 范围内的任意小写字母
# `[A-Z]`  匹配 A 到 Z 范围内的任意大写字母
# `[0-9]`  匹配 0 到 9 范围内的任意数字

# **3. 数量限定符**
# `*`  匹配前面的表达式 0 次或多次
# `+`  匹配前面的表达式 1 次或多次
# `?`  匹配前面的表达式 0 次或 1 次
# `{n}`  匹配前面的表达式恰好 n 次
# `{n,}` 匹配前面的表达式至少 n 次
# `{n,m}` 匹配前面的表达式至少 n 次，至多 m 次

# **4. 位置锚定**
# `^`  匹配字符串的开头（在方括号内表示"非"）
# `$`  匹配字符串的结尾
# `\b` 匹配单词边界
# `\B` 匹配非单词边界

# **5. 分组与引用**
# `()`  创建一个分组，可以提取或引用
# `(?:)` 非捕获分组，不存储匹配结果
# `|`  或运算符，匹配左边或右边的表达式

# **6. 转义字符**
# `\`  转义特殊字符，如 `\.` 匹配实际的点号

# **7. 预定义字符集**
# `\t` 制表符
# `\n` 换行符
# `\r` 回车符
# `\f` 换页符
# `\v` 垂直制表符

#. 贪婪匹配和惰性匹配
# 贪婪匹配 .*：默认情况下，数量限定符是贪婪的，会尽可能多地匹配字符。
# 惰性匹配 .*?：通过在数量限定符后添加 `?`，可以使其变为惰性匹配，只匹配尽可能少的字符

import re

lst = re.findall(r"\d+","110231")
print(lst) 

# 并不常用 findall

it = re.finditer(r"\d+","110231and 1231")
for a in it:
    print(a.group())

# finditer 常用，匹配字符串中所有的内容，用.group()取出内容


s = re.search(r"\d+","abc123defabc456")
print(s.group())
# search 常用，匹配字符串中第一个符合条件的内容，用.group()取


w = re.match(r"\d+","123defabc456")
print(w.group())
# match 不常用，只匹配字符串开头符合条件的内容，用.group()取

o = re.compile(r"\d+")
m = o.finditer ("abc123def456ghi789")
for a in m:
    print(a.group())
# compile 常用，预编译正则表达式，提高匹配效率

